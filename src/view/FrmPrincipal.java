        Frame[] telas = Frame.getFrames();
        for (Frame fr : telas) {
             teste += fr.getClass().getName() + "\n";
             if (fr.getClass().getName().equals("view.FrmVendaPeca")) {
                 JOptionPane.showMessageDialog(null, "Existe!");
                 
             }
        }
        JOptionPane.showMessageDialog(null, teste);
        teste = "";
        
